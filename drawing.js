class DrawScreen {
    constructor(width, height, blockLength, defaultBlock=NoneBlock()) {
        this.width = width;
        this.height = height;
        this.blockLength = blockLength;
        this.defaultBlock = defaultBlock;
        this.screen = this.clear();
    }
    clear() {

        const newScreen = {
            map: mdList([this.width, this.height]),
            players: [],
            npcs: [],
            objects: [],
        };

        for (let x of range(this.width)) {
            for (let y of range(this.height)) {
                newScreen.map[x][y] = this.defaultBlock.draw([x, y]);
            }
        }

        return newScreen;
    }

}